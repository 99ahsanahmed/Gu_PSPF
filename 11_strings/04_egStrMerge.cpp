// Example : 4 String Merge
#include <iostream>
#include <string>
using namespace std;

int main() {
    cout<<"Merging the string";
    string str1 = "Hello";
    string str2 = " World";
    
    string result = str1 + str2;
    cout << "Concatenated String: " << result << endl;
    cout << "Length: " << result.length() << endl;

    return 0;
}
